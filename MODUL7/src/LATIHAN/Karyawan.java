public class Karyawan {
 private String nama;
    private float gaji;
    private int usia;

    public String getNama(){
        return this.nama;
    }

    public void setName(String nama){
        this.nama = nama;
    }

    public float getGaji(){
        return this.gaji;
    }

    public void setGaji(float gaji){
        this.gaji = gaji;
    }

    public int getUsia(){
        return this.usia;
    }

    public void setUsia(int usia){
        this.usia = usia;
    }

}
